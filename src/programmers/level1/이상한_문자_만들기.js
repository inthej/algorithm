function solution(s) {
  let result = ''
  const words = s.split(' ')
  for (const [wordIndex, word] of words.entries()) {
    const isLastIndex = wordIndex === words.length - 1
    const str = word
      .split('')
      .map((s, sIndex) => (sIndex % 2 === 0 ? s.toUpperCase() : s.toLowerCase()))
      .join('')
    result += str
    if (!isLastIndex) {
      result += ' '
    }
  }
  return result
}

